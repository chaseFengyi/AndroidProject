package com.market.model;

public class UserInfoBean {
	private String userId;
	private String userSchoolNum;
	private String userPassword;
	private String userPictureAd;
	private String userName;
	private String userNike;
	private String userSex;
	private String userGrade;

	public String getUserNike() {
		return userNike;
	}

	public void setUserNike(String userNike) {
		this.userNike = userNike;
	}

	public String getUserSex() {
		return userSex;
	}

	public void setUserSex(String userSex) {
		this.userSex = userSex;
	}

	public String getUserGrade() {
		return userGrade;
	}

	public void setUserGrade(String userGrade) {
		this.userGrade = userGrade;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserSchoolNum() {
		return userSchoolNum;
	}

	public void setUserSchoolNum(String userSchoolNum) {
		this.userSchoolNum = userSchoolNum;
	}

	public String getUserPassword() {
		return userPassword;
	}

	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}

	public String getUserPictureAd() {
		return userPictureAd;
	}

	public void setUserPictureAd(String userPictureAd) {
		this.userPictureAd = userPictureAd;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

}
